// 获取翻译文本
export const getFileTypeConfigTranslation = (key, language, params = {}) => {
  const translation = fileTypeConfigTranslations[language]?.[key];
  if (!translation) return key;
  
  // 简单的参数替换
  return translation.replace(/\{(\w+)\}/g, (match, paramKey) => {
    return params[paramKey] || match;
  });
};
